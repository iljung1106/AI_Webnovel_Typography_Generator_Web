def style_doc(doc: Document) -> None:
    section = doc.sections[0]
    section.page_width = Cm(21.0)
    section.page_height = Cm(29.7)
    section.top_margin = Cm(1.8)
    section.bottom_margin = Cm(1.8)
    section.left_margin = Cm(2.0)
    section.right_margin = Cm(2.0)
    normal = doc.styles["Normal"]
    normal.font.name = "Malgun Gothic"
    normal._element.rPr.rFonts.set(qn("w:eastAsia"), "Malgun Gothic")
    normal.font.size = Pt(10.4)
    normal.paragraph_format.line_spacing = 1.33
    normal.paragraph_format.space_after = Pt(5)
    for style_name, size in [("Title", 20), ("Heading 1", 13.5)]:
        st = doc.styles[style_name]
        st.font.name = "Malgun Gothic"
        st._element.rPr.rFonts.set(qn("w:eastAsia"), "Malgun Gothic")
        st.font.size = Pt(size)
        st.font.bold = True
        st.font.color.rgb = ACCENT if style_name == "Heading 1" else INK


def build_doc() -> None:
    doc = Document()
    style_doc(doc)
    doc.add_paragraph("AI 기반 웹소설 타이포그래피 제작 웹앱 중간발표 대본", style="Title")
    p = doc.add_paragraph()
    r = p.add_run("슬라이드 14장 / 10분 발표용")
    r.font.color.rgb = MUTED

    md_lines = ["# AI 기반 웹소설 타이포그래피 제작 웹앱 중간발표 대본", "", "슬라이드 14장 / 10분 발표용", ""]
    for title, lines in SLIDES:
        doc.add_heading(title, level=1)
        md_lines.extend([f"## {title}", ""])
        for line in lines:
            doc.add_paragraph(line)
            md_lines.append(line)
            md_lines.append("")
        md_lines.append("")

    OUT.parent.mkdir(parents=True, exist_ok=True)
    doc.save(OUT)
    MD_OUT.write_text("\n".join(md_lines), encoding="utf-8")
    print(OUT)
    print(MD_OUT)


if __name__ == "__main__":
    build_doc()
